package com.northout.service;
/**
 * Web Service Implementation class
 * @author northout
 *
 */
public class Calservice {

	/**
	 * Addition of param i and j
	 * @param i
	 * @param j
	 * @return
	 */
	public int add(int i, int j) {
		return i+j;
		
	}
	/**
	 * Subtraction of param i and j
	 * @param i
	 * @param j
	 * @return
	 */
	public int sub(int i, int j) {
		return i-j;
		
	}
}
